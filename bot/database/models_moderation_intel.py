class KnownIssue(Base):
    """A known server issue staff have logged (e.g. "EU servers down for maintenance")."""

    __tablename__ = "known_issues"

    id: Mapped[int] = mapped_column(Integer, primary_key=True, autoincrement=True)
    guild_id: Mapped[int] = mapped_column(BigInteger, index=True)
    title: Mapped[str] = mapped_column(String(200))
    description: Mapped[str] = mapped_column(Text)
    is_resolved: Mapped[bool] = mapped_column(Boolean, default=False, index=True)
    created_by: Mapped[int] = mapped_column(BigInteger)
    created_at: Mapped[dt.datetime] = mapped_column(DateTime(timezone=True), server_default=func.now())
    resolved_at: Mapped[dt.datetime | None] = mapped_column(DateTime(timezone=True), nullable=True)


class LinkedAccount(Base):
    """A Discord <-> in-game account link, as recorded by staff/an external linking flow."""

    __tablename__ = "linked_accounts"
    __table_args__ = (UniqueConstraint("guild_id", "discord_user_id", name="uq_linked_accounts_guild_user"),)

    id: Mapped[int] = mapped_column(Integer, primary_key=True, autoincrement=True)
    guild_id: Mapped[int] = mapped_column(BigInteger, index=True)
    discord_user_id: Mapped[int] = mapped_column(BigInteger, index=True)
    ingame_username: Mapped[str] = mapped_column(String(100))
    linked_at: Mapped[dt.datetime] = mapped_column(DateTime(timezone=True), server_default=func.now())


class WhitelistStatus(str, enum.Enum):
    PENDING = "pending"
    APPROVED = "approved"
    DENIED = "denied"


class WhitelistEntry(Base):
    """Whitelist application/status for an in-game username."""

    __tablename__ = "whitelist_entries"
    __table_args__ = (UniqueConstraint("guild_id", "ingame_username", name="uq_whitelist_guild_username"),)

    id: Mapped[int] = mapped_column(Integer, primary_key=True, autoincrement=True)
    guild_id: Mapped[int] = mapped_column(BigInteger, index=True)
    ingame_username: Mapped[str] = mapped_column(String(100))
    discord_user_id: Mapped[int | None] = mapped_column(BigInteger, nullable=True)
    status: Mapped[WhitelistStatus] = mapped_column(Enum(WhitelistStatus), default=WhitelistStatus.PENDING)
    updated_at: Mapped[dt.datetime | None] = mapped_column(DateTime(timezone=True), onupdate=func.now())
    created_at: Mapped[dt.datetime] = mapped_column(DateTime(timezone=True), server_default=func.now())
